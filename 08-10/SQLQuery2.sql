CREATE TABLE Ex01_Fornecedores(
	FornecedorId UNIQUEIDENTIFIER NOT NULL PRIMARY KEY,
	Nome VARCHAR(50) NOT NULL,
	CNPJ VARCHAR(19) NOT NULL
)
GO

CREATE TABLE Ex01_Clientes(
ClienteId UNIQUEIDENTIFIER NOT NULL PRIMARY KEY,
Nome VARCHAR(50) NOT NULL,
CPF VARCHAR(14) NOT NULL,
DataNascimento DATE NOT NULL,
Genero CHAR(1) NOT NULL
)
GO

CREATE TABLE Ex01_Produtos(
ProdutoId UNIQUEIDENTIFIER NOT NULL PRIMARY KEY,
Nome VARCHAR(50) NOT NULL,
Estoque INT NOT NULL,
Unitario Decimal(8,2) NOT NULL
)
GO

